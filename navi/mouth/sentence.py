"""문장 경계 정규식 — 스트리밍 청킹 공통 규칙 (모든 Mouth 어댑터가 공유).

두뇌 토큰 스트림을 문장 단위로 잘라 합성하는 기준. 규칙 두 갈래:
- 반각 종결(.!?)·줄바꿈: 뒤에 공백/버퍼 끝을 요구 — 소수점(3.5)·ASCII 말줄임(...)
  중간에서 끊기지 않는다.
- 전각 종결(。！？…): 공백 없이도 경계 — 일본어 문말은 `。` 뒤에 공백을 두지 않으므로
  공백을 요구하면 2~3문장이 한 청크로 뭉쳐 첫 오디오(TTFA)가 비대해진다.
  연속 부호(……)는 한 덩어리로 소비해 말줄임 중간에서 끊기지 않고, 한국어 관행의
  후행 마침표(….)도 함께 흡수한다.

닫는 따옴표·괄호가 종결부호 뒤에 붙으면 같은 문장에 포함한다.
"""

from __future__ import annotations

import re

SENTENCE_END = re.compile(
    r".*?(?:"
    r"[.!?\n]+[\"'”’)\]]*(?=\s|$)"  # 반각 — 공백/끝 요구 (소수점 보호)
    r"|[。！？…]+[.!?]*[\"'”’)\]]*"  # 전각 — 공백 없이 경계 (일본어 문말)
    r")",
    re.DOTALL,
)
